        public ActionResult UserManagement()
        {
            var user = UserManager.FindByName(User.Identity.Name);
            var userInfo = db.UserInfoes.First(u => u.UserName == User.Identity.Name);
            ViewBag.User = user;
            ViewBag.ImageUrl = userInfo.ImageUrl;
            return View();
        }

        [HttpPost]
        [ValidateInput(false)]
        public async Task<ActionResult> ResendEmail()
        {
            var user = db.Users.Find(User.Identity.GetUserId());
            user.EmailConfirmed = false;
            db.SaveChanges();
            string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
            var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
            await UserManager.SendEmailAsync(user.Id, "确认你的帐户", "请通过单击 <a href=\"" + callbackUrl + "\">這裏</a>来确认你的帐户");
            return RedirectToAction("UserManagement");
        }

        public async Task<ActionResult> ResetEmail()
        {
            var user = db.Users.Find(User.Identity.GetUserId());
            String email = Request.Params["email"];
            if (email != null && email != "")
            {
                user.Email = email;
                user.EmailConfirmed = false;
                db.SaveChanges();
                string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                await UserManager.SendEmailAsync(user.Id, "确认你的帐户", "请通过单击 <a href=\"" + callbackUrl + "\">這裏</a>来确认你的帐户");
            }
            return RedirectToAction("UserManagement");
        }

        [HttpPost]
        public ActionResult UploadPic()
        {
            HttpPostedFileBase file = Request.Files["file"];
            if (file.ContentLength > 0)
            {
                var fileName = Path.GetFileName(file.FileName);
                var path = Path.Combine(Server.MapPath("~/Uploads/avatar"), fileName);
                file.SaveAs(path);

                var userInfo = db.UserInfoes.First(u => u.UserName == User.Identity.Name);
                userInfo.ImageUrl = "/Uploads/avatar/" + fileName;
                db.SaveChanges();
            }
            return RedirectToAction("UserManagement");
        }


        public ActionResult FileManagement()
        {
            List<String> fileNameList = new List<String>();
            List<String> imageUrlList = new List<String>();

            String UserName = User.Identity.Name;
            var userPath = Server.MapPath("~/Uploads/" + UserName + "/");
            if (!Directory.Exists(userPath))
                Directory.CreateDirectory(userPath);
            String[] files = Directory.GetFiles(userPath);
            foreach (var name in files)
            {
                String fileName = Path.GetFileName(name);
                String suffix = fileName.Substring(fileName.LastIndexOf('.')+1);
                String imageUrl = "/icons/";
                if (suffix == "doc" || suffix == "docx")
                    imageUrl += "word.jpg";
                else if (suffix == "xls")
                    imageUrl += "excel.jpg";
                else if (suffix == "java" || suffix == "jar")
                    imageUrl += "java.jpg";
                else if (suffix == "html")
                    imageUrl += "html.jpg";
                else if (suffix == "ppt" || suffix == "pptx")
                    imageUrl += "ppt.jpg";
                else if (suffix == "py")
                    imageUrl += "py.jpg";
                else if (suffix == "zip")
                    imageUrl += "zip.jpg";
                else if (suffix == "rar")
                    imageUrl += "rar.jpg";
                else if (suffix == "mp3")
                    imageUrl += "mp3.jpg";
                else if (suffix == "jpg" || suffix =="png" || suffix == "gif")
                    imageUrl += "image.jpg";
                else
                    imageUrl += "file.jpg";
                fileNameList.Add(fileName);
                imageUrlList.Add(imageUrl);
            }
            ViewBag.fileNameList = fileNameList;
            ViewBag.imageUrlList = imageUrlList;
            return View();
        }

        [HttpPost]
        public ActionResult UploadFile(){
            String UserName = User.Identity.Name;
            var userPath = Server.MapPath("~/Uploads/" + UserName + "/");
            if (!Directory.Exists(userPath))
                Directory.CreateDirectory(userPath);
            for (int i = 0; i < Request.Files.Count; i++ )
            {
                HttpPostedFileBase file = Request.Files[i];
                if (file.ContentLength > 0)
                {
                    var fileName = Path.GetFileName(file.FileName);
                    var path = Path.Combine(Server.MapPath("~/Uploads/" + UserName + "/"), fileName);
                    file.SaveAs(path);
                }
            }
            return RedirectToAction("FileManagement");
        }

        [HttpPost]
        public ActionResult DeleteFile()
        {
            String UserName = User.Identity.Name;
            var userPath = Server.MapPath("~/Uploads/" + UserName + "/");
            var path = userPath + Request.Params["fileName"];
            System.IO.File.Delete(path);
            return RedirectToAction("FileManagement");
        }
